//Hay que requerir la libreria dotenv para leer variables de entorno
require('dotenv').config();
//Me traigo la variable del puerto desde el archivo .env
const PORT = process.env.PORT
// sincronizamos el servidor y la base de datos
//La propiedad alter en true sirve para modificar la estructura y
//la informacion de la base de datos, cuando esta en false no se puede modificar
// la estructura (campos en los modelos)
